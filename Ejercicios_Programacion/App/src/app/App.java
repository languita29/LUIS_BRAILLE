package app;

public class App {

    public static void main(String[] args) {
        for (int num = 1; num <= 10; num++) {
            for (int multi = 0; multi <= 10; multi++) {
                System.out.println(num + " * " + multi + " = " + num * multi);
            }
            System.out.println("---------------------------------");
        }
    }

}
